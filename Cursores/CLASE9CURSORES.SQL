
    set SERVEROUTPUT ON

DECLARE
 
 NOMBRE_PRODUCTO PRODUCTS.PRODUCT_NAME%TYPE ; 
 MODELO_PRODUCTO PRODUCTS.MODEL_YEAR%TYPE ; 
 NOMBRE_CATEGORIA CATEGORIES.CATEGORY_NAME%TYPE;
 CURSOR CDATOS_PRODUCTO IS SELECT PRODUCTS.PRODUCT_NAME,PRODUCTS.MODEL_YEAR,CATEGORIES.CATEGORY_NAME FROM PRODUCTS
 INNER JOIN CATEGORIES ON PRODUCTS.CATEGORY_ID  =CATEGORIES.CATEGORY_ID;

BEGIN

  OPEN CDATOS_PRODUCTO ; 
  
  
  LOOP
    FETCH CDATOS_PRODUCTO INTO  NOMBRE_PRODUCTO,NOMBRE_CATEGORIA,MODELO_PRODUCTO; 
    
    
    EXIT WHEN CNOMBRE_PRODUCTO%NOTFOUND;
    DBMS_OUTPUT.PUT_LINE('El nombre del producto es : ' || NOMBRE_PRODUCTO)  ;
     DBMS_OUTPUT.PUT_LINE('la canitdad de registros es : ' ||CNOMBRE_PRODUCTO%ROWCOUNT) ; 
  END LOOP ; 
  
  CLOSE CNOMBRE_PRODUCTO ; 



END   ;